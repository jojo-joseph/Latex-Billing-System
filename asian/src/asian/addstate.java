    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        Connection conn=null; 
        conn = dbconnection.getConnected();
        String name,supplier,shipped;
        name =this.jTextField1.getText();
        supplier = this.jTextArea1.getText();
        shipped = this.jTextArea2.getText();
        
        String query;
        
        try {
            query = "insert into adduser(name,supplier,shipped)  VALUES(?,?,?)";
           
            PreparedStatement pst= conn.prepareStatement(query);
            pst.setString(1,name);
            pst.setString(2,supplier);
            pst.setString(3,shipped);
           
            pst.execute();
             JOptionPane.showMessageDialog(null, "Data Updated");
            jTextField1.setText("");
            jTextArea1.setText("");
            jTextArea2.setText("");
         conn.close();
        } catch (SQLException ex) {
            Logger.getLogger(addstate.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Please Insert Value");
        }
    }//GEN-LAST:event_jButton9ActionPerformed

